import { randomBytes } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { createClient } from "@supabase/supabase-js";

const CREDENTIAL_PATH = ".env.demo-users.local";
const USERS = [
  {
    key: "ADMIN",
    email: "admin@waferwatch.local",
    displayName: "WaferWatch Admin",
    profileRole: "admin",
    memberRole: "owner"
  },
  {
    key: "WILLIAM",
    email: "william@waferwatch.local",
    displayName: "William Xu",
    profileRole: "admin",
    memberRole: "owner"
  },
  {
    key: "VIEWER",
    email: "viewer@waferwatch.local",
    displayName: "WaferWatch Viewer",
    profileRole: "viewer",
    memberRole: "viewer"
  }
];

function requiredEnv(name) {
  const value = process.env[name]?.trim();
  if (!value) {
    throw new Error(`Missing required environment variable: ${name}`);
  }

  return value;
}

function serviceRoleKey() {
  const key = process.env.SUPABASE_SERVICE_ROLE_KEY?.trim() || process.env.SUPABASE_SECRET_KEY?.trim();
  if (!key) {
    throw new Error("Missing SUPABASE_SERVICE_ROLE_KEY or SUPABASE_SECRET_KEY.");
  }

  return key;
}

function randomPassword() {
  return `Ww-${randomBytes(18).toString("base64url")}-26`;
}

async function readExistingCredentials() {
  try {
    const content = await readFile(CREDENTIAL_PATH, "utf8");
    return Object.fromEntries(
      content
        .split(/\r?\n/)
        .map((line) => line.trim())
        .filter((line) => line && !line.startsWith("#") && line.includes("="))
        .map((line) => {
          const index = line.indexOf("=");
          return [line.slice(0, index), line.slice(index + 1)];
        })
    );
  } catch {
    return {};
  }
}

async function writeCredentials(credentials) {
  const lines = [
    "# Generated by scripts/seed-demo-users.mjs. This file is gitignored.",
    ...USERS.flatMap((user) => [
      `WAFERWATCH_${user.key}_EMAIL=${user.email}`,
      `WAFERWATCH_${user.key}_PASSWORD=${credentials[`WAFERWATCH_${user.key}_PASSWORD`]}`
    ]),
    ""
  ];

  await writeFile(CREDENTIAL_PATH, lines.join("\n"), "utf8");
}

async function findUserByEmail(supabase, email) {
  let page = 1;

  while (page < 100) {
    const { data, error } = await supabase.auth.admin.listUsers({ page, perPage: 100 });
    if (error) {
      throw error;
    }

    const found = data.users.find((user) => user.email?.toLowerCase() === email.toLowerCase());
    if (found) {
      return found;
    }

    if (data.users.length < 100) {
      return null;
    }

    page += 1;
  }

  return null;
}

async function upsertAuthUser(supabase, user, password) {
  const existing = await findUserByEmail(supabase, user.email);
  if (existing) {
    const { data, error } = await supabase.auth.admin.updateUserById(existing.id, {
      password,
      email_confirm: true,
      user_metadata: {
        display_name: user.displayName,
        role: user.profileRole
      }
    });

    if (error) {
      throw error;
    }

    return data.user;
  }

  const { data, error } = await supabase.auth.admin.createUser({
    email: user.email,
    password,
    email_confirm: true,
    user_metadata: {
      display_name: user.displayName,
      role: user.profileRole
    }
  });

  if (error) {
    throw error;
  }

  return data.user;
}

async function main() {
  const supabase = createClient(requiredEnv("NEXT_PUBLIC_SUPABASE_URL"), serviceRoleKey(), {
    auth: {
      autoRefreshToken: false,
      persistSession: false
    }
  });
  const existingCredentials = await readExistingCredentials();
  const credentials = {
    ...Object.fromEntries(
      USERS.map((user) => {
        const key = `WAFERWATCH_${user.key}_PASSWORD`;
        return [
          key,
          process.env[key]?.trim() ||
            existingCredentials[key] ||
            randomPassword()
        ];
      })
    )
  };
  const projectResult = await supabase.from("projects").select("id").order("updated_at", { ascending: false });

  if (projectResult.error) {
    throw projectResult.error;
  }

  for (const user of USERS) {
    const password = credentials[`WAFERWATCH_${user.key}_PASSWORD`];
    const authUser = await upsertAuthUser(supabase, user, password);

    const profileResult = await supabase
      .from("profiles")
      .upsert(
        {
          id: authUser.id,
          email: user.email,
          display_name: user.displayName,
          role: user.profileRole,
          lab_group: "McMaster Quantum Photonic Group",
          is_active: true
        },
        { onConflict: "id" }
      );

    if (profileResult.error) {
      throw profileResult.error;
    }

    const personResult = await supabase
      .from("process_people")
      .upsert(
        {
          display_name: user.displayName,
          profile_id: authUser.id,
          is_active: true
        },
        { onConflict: "display_name" }
      );

    if (personResult.error) {
      throw personResult.error;
    }

    const memberRows = (projectResult.data ?? []).map((project) => ({
      project_id: project.id,
      user_id: authUser.id,
      role: user.memberRole
    }));

    if (memberRows.length) {
      const memberResult = await supabase.from("project_members").upsert(memberRows, {
        onConflict: "project_id,user_id"
      });

      if (memberResult.error) {
        throw memberResult.error;
      }
    }

    console.log(`${user.email}: ${user.profileRole}, ${user.memberRole} on ${memberRows.length} project(s)`);
  }

  await writeCredentials(credentials);
  console.log(`Credentials written to ${CREDENTIAL_PATH}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
